def add():
    print("add two numbers")
    x = input("What is the first number\n>?")
    x = int(x)
    y = input("What is the second number\n>?")
    y = int(y)
    print(str(x) + " + " + str(y) + " = " + str(x + y)) 

def subtract():
    print("subtract two numbers")
    x = input("What is the first number\n>?")
    x = int(x)
    y = input("What is the second number\n>?")
    y = int(y)
    print(str(x) + " - " + str(y) + " = " + str(x - y)) 

def multiply():
    print("multiply two numbers")
    x = input("What is the first number\n>?")
    x = int(x)
    y = input("What is the second number\n>?")
    y = int(y)
    print(str(x) + " x " + str(y) + " = " + str(x * y)) 